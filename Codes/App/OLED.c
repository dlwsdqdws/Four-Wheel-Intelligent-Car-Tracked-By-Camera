void OLED_WrDat(uint8 data)                      //写数据函数
{
    uint8 i=8;
    OLED_DC_H;;
    OLED_SCL_L;;
    while(i--)
    {
        if(data&0x80)
        {
            OLED_SDA_H;
        }
        else
        {
            OLED_SDA_L;
        }
        OLED_SCL_H;
        asm("nop");;
        OLED_SCL_L;;
        data<<=1;
    }
}
void OLED_WrCmd(uint8 cmd)                    //写命令函数
{
    uint8 i=8;
    OLED_DC_L;;
    OLED_SCL_L;;
    while(i--)
    {
        if(cmd&0x80)
        {
            OLED_SDA_H;
        }
        else
        {
            OLED_SDA_L;;
        }
        OLED_SCL_H;;
        asm("nop");;
        OLED_SCL_L;;
        cmd<<=1;;
    }
}
void OLED_Set_Pos(uint8 x, uint8 y)   //设定点的坐标函数
{
    OLED_WrCmd(0xb0+y);
    OLED_WrCmd(((x&0xf0)>>4)|0x10);
    OLED_WrCmd((x&0x0f)|0x01);
}
void OLED_Fill(uint8 bmp_data)        //屏幕覆盖
{
    uint8 y,x;

    for(y=0; y<8; y++)
    {
        OLED_WrCmd(0xb0+y);
        OLED_WrCmd(0x01);
        OLED_WrCmd(0x10);
        for(x=0; x<X_WIDTH; x++)
            OLED_WrDat(bmp_data);
    }
}
void OLED_CLS(void)                    //清屏
{
    uint8 y,x;
    for(y=0; y<8; y++)
    {
        OLED_WrCmd(0xb0+y);
        OLED_WrCmd(0x01);
        OLED_WrCmd(0x10);
        for(x=0; x<X_WIDTH; x++)
            OLED_WrDat(0);
    }
}
void OLED_DLY_ms(uint16 ms)
{
    uint16 a;
    while(ms)
    {
        a=1335;
        while(a--);
        ms--;
    }
    return;
}

