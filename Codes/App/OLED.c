//函数名： void OLED_PutPixel(uint8 x,uint8 y)
//功能描述：绘制一个点（x,y）
//参数：真实坐标值(x,y),x的范围0～127，y的范围0～64
//返回：无
//==============================================================
void OLED_PutPixel(uint8 x,uint8 y)
{
    uint8 data1;  //data1当前点的数据

    OLED_Set_Pos(x,y);
    data1 = 0x01<<(y%8);
    OLED_WrCmd(0xb0+(y>>3));
    OLED_WrCmd(((x&0xf0)>>4)|0x10);
    OLED_WrCmd((x&0x0f)|0x00);
    OLED_WrDat(data1);
}
